# Lag et quiz-spill
# Denne oppgaven kan løses på mange forskjellige måter, men noen krav stilles:
    # Programmet må vise spørsmål til brukeren
    # Brukeren må kunne svare på spørmsålene
    # Brukeren skal få poeng for riktige svar
    # Brukeren skal få tilbakemeldinger på om svarene er riktige eller feil
    # Til slutt skal brukeren få en karakter (f. eks A-F) basert på den totale poengsummen 
# Prøv å lage programmet så dynamisk som mulig!
# Det vil si at man bør ikke trenge å gjøre store forandringer i koden dersom flere spørsmål skal legges til eller fjernes 

